/* JS tiene tres forma de declarar variables:
var
let
const
*/

var nombre="Kuchao";
console.log(nombre);

// se puede renombrar variables con var
var nombre="Victor";
console.log(nombre);

// Usando let (no se pueden renombrar variables)
let user="HuronMarron";
//let user="Gato";  
console.log(user);

// const equivale a "final" en java. Solo se declara una vez y no se modifica

// Ejemplo 1 de operaciones con datos duros
let suma = 25+5;
let resta = 10-5;
let mult = 30*3;
let div = 50/2;

console.log(suma);
console.log(resta);
console.log(mult);
console.log(div);

